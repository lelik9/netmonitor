package SNMP;

public class ResponseResult
    {
	    private String NextOID;
	    private String Char;
	    public String getNextOID()
		{
			return NextOID;
		}
	    public void setNextOID(String nextOID)
		{
			NextOID = nextOID;
		}
	    public String getChar()
		{
			return Char;
		}
	    public void setChar(String c)
		{
			Char = c;
		}

    }
